    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String userName=userNameE.getText();
        String name=nameE.getText();
        String phone=phoneE.getText();
        String birth=birthE.getText();  
        String securityQuestions=securityQuestionsE.getText();
        String answer=answerE.getText();
        String password=passwordE.getText();
        String role=roleE.getText();
        String sql="select * from Account where UserName='"+userName+"'";
        if((userNameE.getText()).equals("")){
                Failed("Please Fill User Name");
            }
            else if ((nameE.getText()).equals("")){
                Failed("Please Fill Name");
            }
            else if ((phoneE.getText()).equals("")){
                Failed("Please Fill Your Phone");
            }
            else if ((birthE.getText()).equals("")){
                Failed("Please Fill Birth");
            }
            else if ((securityQuestionsE.getText()).equals("")){
                Failed("Please Fill Security Question");
            }
            else if ((answerE.getText()).equals("")){
                Failed("Please Answer The Security Questions");
            }
            else if ((passwordE.getText().equals(""))){
                Failed("Please Fill Password");
            }
            else if ((roleE.getText().equals(""))){
                Failed("Please Fill Role");
            }
            else{
                try{
                    pst=conn.prepareStatement(sql);
                    rs=pst.executeQuery();
                    if ((rs.getString(1)).equals(userName)){
                        try {           
                            PreparedStatement st = conn.prepareStatement("UPDATE Account SET Name=?, YouPhone = ?, Birth = ?, SecurityQuestions = ?, Answer = ?, Password = ?, Role = ? WHERE UserName = ?");
                            st.setString(1,name);
                            st.setString(2,phone);
                            st.setString(3,birth);
                            st.setString(4,securityQuestions);
                            st.setString(5,answer);
                            st.setString(6,password);
                            st.setString(7,role);
                            st.setString(8,userName);
                            st.executeUpdate();
                            Completed("Update Completed!");
                            rs.close();
                            pst.close();
                            st.close();
                            loadData();
                        }
                        catch (Exception e ) {
                            Failed( "Update Failed");      
                        }           
                    }
                    else{
                        Failed("Incorrect User Name For Update - Please Try Again!");
                    }

                }catch(Exception e){
                    Failed("Update Failed - Not Contain User Name");
                }
            }
    }//GEN-LAST:event_btnSaveActionPerformed

