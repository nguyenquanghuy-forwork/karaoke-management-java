    private void loadData(){
        String[] arry={"User Name","Name","Phone","Birth","Security Question","Answer","Password","Role"};
            DefaultTableModel model=new DefaultTableModel(arry,0);
            tableE.setModel(model);
        String sql="select * from Account order by Username";
            try{
                pst=conn.prepareStatement(sql);
                rs=pst.executeQuery();
                while(rs.next()){
                    Vector vector=new Vector();
                    vector.add(rs.getString("Username").trim());
                    vector.add(rs.getString("Name").trim());
                    vector.add(rs.getString("YouPhone").trim());
                    vector.add(rs.getString("Birth").trim());
                    vector.add(rs.getString("SecurityQuestions").trim());
                    vector.add(rs.getString("Answer").trim());
                    vector.add(rs.getString("Password").trim());
                    vector.add(rs.getString("Role").trim());
                    model.addRow(vector);
                }
                pst.close();
                rs.close();
            }
            catch(Exception ex){
                ex.printStackTrace();
            }
    }

