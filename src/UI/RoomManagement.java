    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(code.getText().equals("")){
            Failed("- Code Room Need Fill -");
        }else
         if(name.getText().equals("")){
            Failed("- Room Name Need Input -");  
        }else
         if(type.getText().equals("")){
            Failed("- Type of Room Need Input -");  
        }else
         if(price.getText().equals("")){
            Failed("- Price of Room Need Input -");  
        }
        else{
            try{
                String sql="Insert into RoomInformation (RoomCode, Name, Type, Price, Status) values (?,?,?,?,?)";
                pst=conn.prepareStatement(sql);
                pst.setString(1,code.getText());
                pst.setString(2,name.getText());
                pst.setString(3,type.getText());
                pst.setString(4,price.getText());
                pst.setString(5,"Empty");
                pst.execute();
                pst.close();
                Completed("Add Room Completed!");
                loadDataWaitingList();
                reset();
             }catch(Exception e){
                Failed("Room Already Exists ");
             }    
         }     
    }//GEN-LAST:event_btnAddActionPerformed

