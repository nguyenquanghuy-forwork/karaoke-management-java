    private void loadDataWaitingList(){
        String[] arry={"Room Code","Room Name","Type","Price"};
            DefaultTableModel model=new DefaultTableModel(arry,0);
            roomList.setModel(model);
        String sql="select * from RoomInformation order by RoomCode, Name";
            try{
                pst=conn.prepareStatement(sql);
                rs=pst.executeQuery();
                while(rs.next()){
                    Vector vector=new Vector();
                    vector.add(rs.getString("RoomCode").trim());
                    vector.add(rs.getString("Name").trim());
                    vector.add(rs.getString("Type").trim());
                    vector.add(rs.getString("Price").trim());
                    model.addRow(vector);
                }
                roomList.setModel(model);
                pst.close();
                rs.close();;
            }
            catch(Exception ex){
                Failed("Load Data Waiting List Unknown Error");
            }
    }
    
    
