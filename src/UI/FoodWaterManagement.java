    private void loadData(){
        String[] arry={"Code","Type","Name","Price (vnd)"};
            DefaultTableModel model=new DefaultTableModel(arry,0);
            tableFoodWater.setModel(model);
        String sql="select * from FoodWaterManagement order by Type";
            try{
                pst=conn.prepareStatement(sql);
                rs=pst.executeQuery();
                while(rs.next()){
                    Vector vector=new Vector();
                    vector.add(rs.getString("Code").trim());
                    vector.add(rs.getString("Type").trim());
                    vector.add(rs.getString("Name").trim());
                    vector.add(rs.getString("Price").trim());
                    model.addRow(vector);
                }
            }
            catch(Exception ex){
                Failed("Load Data - Wrong");
            }
    }

