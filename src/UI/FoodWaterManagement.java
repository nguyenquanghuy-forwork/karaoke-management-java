    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String sql="select * from FoodWaterManagement where Code='"+code.getText()+"'";
        if((code.getText()).equals("")){
                Failed("Please Fill Code");
            }
            else if (((String)type.getSelectedItem()).equals("")){
                Failed("Please Fill Type");
            }
            else if ((name.getText()).equals("")){
                Failed("Please Fill Name");
            }
            else if ((price.getText()).equals("")){
                Failed("Please Fill Price");
            }
            else{
                try{
                    pst=conn.prepareStatement(sql);
                    rs=pst.executeQuery();
                    if ((rs.getString(1)).equals(code.getText())){
                        try {           
                            PreparedStatement st = conn.prepareStatement("UPDATE FoodWaterManagement SET Type=?, Name = ?, Price = ? WHERE Code = ?");
                            st.setString(1,(String)type.getSelectedItem());
                            st.setString(2,name.getText());
                            st.setString(3,price.getText());
                            st.setString(4,code.getText());
                            st.executeUpdate();
                            Completed("Update Completed!");
                            rs.close();
                            pst.close();
                            st.close();
                            loadTextBox();
                        }
                        catch (Exception e ) {
                            Failed( "Update Failed");      
                        }           
                        }
                    else{
                        Failed("Incorrect Code For Update - Please Try Again!");
                    }
            
                }catch(Exception e){
                    Failed("Update Failed - Not Contain Code Food");
                }
        }
        loadData();
    }//GEN-LAST:event_btnEditActionPerformed
